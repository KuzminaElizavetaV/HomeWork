Console.Write("Введите число от 1 до 7: ");
int  NumberDay = int.Parse(Console.ReadLine());
if (NumberDay == 1) Console.Write("Понедельник");
if (NumberDay == 2) Console.Write("Вторник");
if (NumberDay == 3) Console.Write("Среда");
if (NumberDay == 4) Console.Write("Четверг");
if (NumberDay == 5) Console.Write("Пятница");
if (NumberDay == 6) Console.Write("Суббота");
if (NumberDay == 7) Console.Write("Воскресенье");
